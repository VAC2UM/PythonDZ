# Самое популярное решение
def main(z, x):
    n = len(z)
    z.insert(0, 0)
    x.insert(0, 0)
    summa = 0
    for i in range(1, n + 1):
        summa += (51 * (z[i] + z[i] ** 3
        + 24 * x[i] ** 2) ** 6)
    return 88 * summa


